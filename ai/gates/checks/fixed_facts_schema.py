#!/usr/bin/env python3
"""fixed-facts-schema: a saved emulator records the science it was born under.

An emulator answers questions about one cosmology, over one region of one
parameter space. A saved emulator used to record neither. It kept the names of
the parameters that were sampled and nothing about the world those names lived
in: not what was held fixed while they varied, not the interval each ranged
over, not which dataset the weights were fitted to. So a matter-power emulator
trained with a varying dark-energy equation of state could be handed to a
likelihood that assumes a cosmological constant, and it would answer.

This check drives the record that closes that hole (emulator/fixed_facts.py).
It proves, on real HDF5 files:

  the round trip     both blocks survive a write and a read unchanged
  the two-way check  the blocks in the file must still match the producer's own
                     text stored beside them, so a record rewritten anywhere
                     between the generator and the file is caught
  the refusals       a file with no record, a record in a grammar this code does
                     not know, a coordinate that is both sampled and held fixed,
                     and a whitening geometry that disagrees with the record
                     about the parameter order, are each refused rather than
                     guessed at, and each refusal says what to do about it
  the identity       the dataset's identity really is the digest of the chain
                     that was published, recomputed here from the bytes

It then proves the three comparison laws the record exists to make possible.
They ask three different questions, and conflating them is how the consumers of
these artifacts ended up comparing parameter-axis names and calling it a
cosmology check:

  vertical    does this artifact belong to the cosmology being sampled? every
              coordinate the artifact held fixed must equal the value the
              resolved global model gives for it
  horizontal  do these two artifacts belong to each other? one dataset, one
              cosmology, one list of sampled coordinates, compared by equality
  domain      may this artifact be asked about this point? the point must lie
              inside the region the artifact was trained over, and the region a
              PAIR may be asked about is the overlap of the two, never the union

and it carries a mutation arm for every law, which are the point of the whole
file: a check that cannot go red proves nothing. Each arm breaks one law on
purpose, confirms the leg that guards it goes red, and puts the law back.

The live proof that a REAL emulator, with real weights, survives the same round
trip needs torch and a trained network, and is owned by the save/rebuild gate.
This check owns the record's own laws, and needs no accelerator to prove them.

PS: a "sidecar" is a small companion file written next to a data file, sharing
its name stem, the way a chain's .paramnames sits beside its .1.txt. "Sampled"
parameters are the ones the generator drew values for; "fixed" ones are held at
a constant value while it draws. A parameter's "support" is the interval it was
drawn from, and the "served support" of a pair of emulators is the region both
halves were trained over. The "resolved model" is the cosmology a chain is
actually being sampled under, with every default the input YAML left unstated
already filled in. A "test double" is an emulator a gate builds in memory out of
noise to prove the machinery works: it was generated by nobody, it describes no
cosmology, it declares no support, and it must never reach a likelihood. A
"mutation arm" breaks the code deliberately to prove a check would notice.

Spec: ai/notes/artifacts-inference-warmstart.md, "fixed-facts-schema: a saved
emulator records the science it was born under."
"""
