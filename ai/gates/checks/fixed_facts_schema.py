def blocks_of(text):
    """Parse one sidecar's text into the two blocks the laws are asked about.

    Arguments:
      text = the sidecar's text, from build_sidecar or synthetic_sidecar.

    Returns:
      the two blocks, validated, as fixed_facts.parse_sidecar returns them.
    """
    return fixed_facts.parse_sidecar(text=text, where="fixture")


def write_artifact(path, text, schema_version=fixed_facts.SCHEMA_VERSION):
    """Write one HDF5 file carrying nothing but the record, and its version.

    Arguments:
      path           = where to write the file.
      text           = the producer sidecar's text to copy into it.
      schema_version = the version to announce, so a caller can forge an old
                       one.

    Returns:
      None.
    """
    import h5py
    with h5py.File(path, "w") as f:
        f.attrs["schema_version"] = schema_version
        fixed_facts.write_h5(f=f, sidecar_text=text)


def read_artifact(path):
    """Read the record back out of one HDF5 file, enforcing every law.

    Arguments:
      path = the file to read.

    Returns:
      the two blocks, as fixed_facts.read_h5 returns them.
    """
    import h5py
    with h5py.File(path, "r") as f:
        version = int(f.attrs["schema_version"])
        return fixed_facts.read_h5(f=f, schema_version=version, where=path)


def refusal_of(fn):
    """Run something that must refuse, and hand back the refusal's words.

    Arguments:
      fn = a callable taking no arguments.

    Returns:
      the refusal's message, or None when it did not refuse.
    """
    try:
        fn()
    except ValueError as exc:
        return str(exc)
    return None


def says_how_to_fix(message):
    """Judge whether a refusal tells the reader what to do about it.

    A message that says a file is incompatible and stops is not a refusal, it
    is a shrug. Every refusal in this record names the way out.

    Arguments:
      message = the refusal's message, or None.

    Returns:
      True when the message carries the migration instruction.
    """
    if message is None:
        return False
    return "Re-generate the dataset" in message


def says_what_to_do(message, remedy):
    """Judge whether a comparison law's refusal names the way out.

    The record's own refusals all end in the same instruction, and the helper
    above looks for it: re-generate the dataset, because the file is what is
    wrong. A comparison law cannot say that. The file is fine; the way it is
    being served is not, and the way out differs with the law. Serve the pair
    from one dump. Sample inside the region. Resolve the coordinate in the model
    being sampled. So the caller names the remedy it expects to read, and this
    asks only that the refusal carries it.

    Arguments:
      message = the refusal's message, or None.
      remedy  = the words the refusal must carry to be an instruction and not a
                shrug.

    Returns:
      True when the message names that way out.
    """
    if message is None:
        return False
    return remedy in message


def check_round_trip(work):
    """Both blocks survive the write and the read unchanged.

    Arguments:
      work = a scratch directory to write into.

    Returns:
      None.
    """
    text = sample_sidecar()
    path = os.path.join(work, "round-trip.h5")
    write_artifact(path=path, text=text)
    blocks = read_artifact(path)

    facts = blocks[fixed_facts.FIXED_FACTS_GROUP]
    domain = blocks[fixed_facts.INPUT_DOMAIN_GROUP]

    report("the sampled names survive the round trip, in order",
           domain["names"] == NAMES,
           repr(domain["names"]))
    # a boolean must come back a boolean. HDF5 has no Python types, and in
    # Python True == 1, so an integer that slipped in where a boolean belongs
    # would compare equal to True and never be noticed.
    report("a fixed fact keeps its type, not just its value",
           facts["flat_only"] is True,
           "flat_only is " + repr(facts["flat_only"]))
    report("the cosmology held fixed survives the round trip",
           facts["cosmology_fixed"]["mnu"] == 0.06
           and facts["cosmology_fixed"]["TCMB"] == 2.7255,
           repr(facts["cosmology_fixed"]))
    # the sampled coordinate is NOT among the pinned ones. The record cannot
    # answer "what was w?" two different ways.
    report("a sampled coordinate is absent from the fixed cosmology",
           "w" not in facts["cosmology_fixed"],
           "cosmology_fixed holds " + repr(sorted(facts["cosmology_fixed"])))
    low, high = fixed_facts.domain_bounds(blocks, "w")
    report("a support reads back as the interval it was drawn from",
           low < -1.29 and high > -0.71,
           "w in [" + repr(low) + ", " + repr(high) + "]")
    # the shortest decimal that reads back as the same float32. A coarser
    # format would collapse two distinct bounds into one string.
    report("two bounds that differ in the last float32 digit stay distinct",
           fixed_facts.format_value(70.00001)
           != fixed_facts.format_value(70.00002),
           fixed_facts.format_value(70.00001) + " vs "
           + fixed_facts.format_value(70.00002))


def check_rewritten_record(work):
    """A record rewritten after the file was written is caught and refused.

    The file carries the producer's own text AND the blocks parsed from it. The
    reader checks them against each other in both directions. This is what makes
    "copied verbatim, never re-derived" a statement that can be checked rather
    than a promise that must be trusted.

    Arguments:
      work = a scratch directory to write into.

    Returns:
      None.
    """
    import h5py

    path = os.path.join(work, "rewritten.h5")
    write_artifact(path=path, text=sample_sidecar())
    # edit ONE fact in the stored block, leaving the producer's text alone: the
    # forgery a re-derivation would produce if training became a second author.
    with h5py.File(path, "r+") as f:
        group = f[fixed_facts.FIXED_FACTS_GROUP][ "cosmology_fixed"]
        group.attrs["mnu"] = 0.12

    message = refusal_of(lambda: read_artifact(path))
    report("a fact edited after the write is refused, not believed",
           message is not None and "does not match the producer text" in message,
           (message or "accepted").splitlines()[0][:72])
    report("the refusal shows the file's record beside the producer's",
           message is not None and "the file's record" in message
           and "the producer said" in message,
           "both sides named")

    # the other direction: swap the producer's text under blocks that no longer
    # match it. One-directional checking would miss this.
    other = os.path.join(work, "swapped.h5")
    write_artifact(path=other, text=sample_sidecar())
    with h5py.File(other, "r+") as f:
        del f[fixed_facts.SIDECAR_DATASET]
        dt = h5py.string_dtype(encoding="utf-8")
        f.create_dataset(fixed_facts.SIDECAR_DATASET,
                         data=sample_sidecar(dataset_id="sha256:" + "b" * 64),
                         dtype=dt)
    swapped = refusal_of(lambda: read_artifact(other))
    report("a producer text swapped under its blocks is refused",
           swapped is not None and "does not match the producer text" in swapped,
           (swapped or "accepted").splitlines()[0][:72])


def check_missing_record(work):
    """A file with no record at all is refused, and told how to get one.

    Arguments:
      work = a scratch directory to write into.

    Returns:
      None.
    """
    import h5py

    for group in (fixed_facts.FIXED_FACTS_GROUP,
                  fixed_facts.INPUT_DOMAIN_GROUP,
                  fixed_facts.SIDECAR_DATASET):
        path = os.path.join(work, "no-" + group + ".h5")
        write_artifact(path=path, text=sample_sidecar())
        with h5py.File(path, "r+") as f:
            del f[group]
        message = refusal_of(lambda: read_artifact(path))
        report("a file missing its " + group + " is refused",
               message is not None,
               (message or "accepted").splitlines()[0][:60])
        report("that refusal says how to fix it",
               says_how_to_fix(message),
               "names the re-save path")


def check_legacy_version(work):
    """A record in a grammar this code does not know is refused.

    Both the schema as a whole and each block carry a version. A file written
    before a key existed and a file whose writer forgot that key are different
    files, and a reader that cannot tell them apart will eventually read one as
    the other. So an unknown version is refused rather than guessed at.

    Arguments:
      work = a scratch directory to write into.

    Returns:
      None.
    """
    blocks = fixed_facts.parse_sidecar(text=sample_sidecar(), where="fixture")

    # the two legacy schema versions. Neither carried a record at all, so
    # neither can say which cosmology it was trained under.
    for old in (1, 2):
        message = refusal_of(
            lambda v=old: fixed_facts.validate(blocks=blocks,
                                               where="a version " + str(v)
                                                     + " file",
                                               schema_version=v))
        report("a schema version " + str(old) + " file is refused",
               message is not None,
               (message or "accepted").splitlines()[0][:60])
        report("the version " + str(old) + " refusal says how to migrate",
               says_how_to_fix(message),
               "names the re-save path")

    # a block written in a grammar from the future is refused for the same
    # reason, in the same words.
    future = fixed_facts.parse_sidecar(text=sample_sidecar(), where="fixture")
    future[fixed_facts.FIXED_FACTS_GROUP]["block_version"] = 99
    message = refusal_of(
        lambda: fixed_facts.validate(blocks=future, where="a future file"))
    report("a block grammar this code does not know is refused",
           message is not None and "does not know" in message,
           (message or "accepted").splitlines()[0][:60])

    # the current version is accepted. A check that only ever refuses proves
    # nothing about the file it is supposed to let through.
    accepted = refusal_of(
        lambda: fixed_facts.validate(blocks=blocks, where="the current file"))
    report("the current schema version is accepted",
           accepted is None,
           "version " + str(fixed_facts.SCHEMA_VERSION) + " reads")


def check_sampled_and_fixed():
    """A coordinate cannot be both sampled and held fixed.

    If it were, the two halves of the record would disagree about the same
    number: one would pin it, the other would let it range. The file would then
    answer "what was w?" differently depending on which half was read.

    Returns:
      None.
    """
    message = refusal_of(
        lambda: fixed_facts.build_sidecar(
            dataset_id="sha256:" + "c" * 64,
            generator="dataset_generator_mps",
            family="grid2d",
            # w is sampled below, and pinned here. That is the contradiction.
            cosmology_fixed={"mnu": 0.06, "w": -1.0},
            neutrino_convention="degenerate",
            flat_only=True,
            dark_energy_law="constant-w",
            dark_energy_inputs=["w"],
            cl_units=fixed_facts.NOT_APPLICABLE,
            base_identity=fixed_facts.NOT_APPLICABLE,
            names=NAMES,
            requested=REQUESTED,
            resolved=RESOLVED))
    report("a coordinate both sampled and held fixed is refused at publication",
           message is not None and "both sampled and held fixed" in message,
           (message or "accepted").splitlines()[0][:66])
    report("the refusal names the coordinate and both of its values",
           message is not None and "'w'" in message and "-1.0" in message,
           "names w, its pinned value, and its sampled interval")


def check_parameter_order():
    """The whitening geometry and the record must agree on the parameter order.

    The emulator's input geometry carries its own copy of the sampled names,
    because that is the order its whitening matrices were built in. If it ever
    disagrees with the record, every incoming value is paired with the wrong
    parameter's column: the predictions are then confidently wrong, and nothing
    about the numbers looks unusual. Nothing compared the two lists before.

    Returns:
      None.
    """
    blocks = fixed_facts.parse_sidecar(text=sample_sidecar(), where="fixture")

    same = refusal_of(
        lambda: fixed_facts.check_names_match(geometry_names=NAMES,
                                              blocks=blocks,
                                              where="a faithful emulator"))
    report("a geometry whose order matches the record is accepted",
           same is None,
           "the orders agree")

    # a PERMUTATION: the same names, the same count, a different order. A check
    # that counted the names, or compared them as a set, would let this through,
    # and it is the case that silently corrupts every prediction.
    permuted = [NAMES[1], NAMES[0]] + NAMES[2:]
    message = refusal_of(
        lambda: fixed_facts.check_names_match(geometry_names=permuted,
                                              blocks=blocks,
                                              where="a permuted emulator"))
    report("a geometry whose order is permuted is refused",
           message is not None and "disagree about the sampled parameters"
           in message,
           repr(permuted[:2]) + " against " + repr(NAMES[:2]))
    report("the refusal prints both orders, so the swap is visible",
           message is not None and repr(permuted) in message
           and repr(NAMES) in message,
           "both orders named")


def check_dataset_identity(work):
    """The dataset's identity is the digest of the chain that was published.

    Two independent generator runs can agree on every fixed fact and every
    bound and still be different datasets: same cosmology, same priors, a
    different seed. Comparing the facts cannot tell them apart. The chain's own
    bytes can, because they are the drawn sample.

    Arguments:
      work = a scratch directory to write into.

    Returns:
      None.
    """
    first = os.path.join(work, "params.1.txt")
    with open(first, "w") as f:
        f.write("# weights lnp omegam H0 ns logA w chi2*\n")
        f.write("1.0 1.0 3.0e-01 7.0e+01 9.6e-01 3.0e+00 -1.0e+00 0.0\n")

    # the same facts, the same bounds, a different draw. This is the pair the
    # identity exists to tell apart.
    second = os.path.join(work, "other.1.txt")
    with open(second, "w") as f:
        f.write("# weights lnp omegam H0 ns logA w chi2*\n")
        f.write("1.0 1.0 3.1e-01 7.0e+01 9.6e-01 3.0e+00 -1.0e+00 0.0\n")

    one = fixed_facts.chain_digest(first)
    again = fixed_facts.chain_digest(first)
    two = fixed_facts.chain_digest(second)

    report("the identity recomputes to the same digest from the same bytes",
           one == again,
           one[:23] + "...")
    report("two different draws carry different identities",
           one != two,
           "the digests differ")
    report("the identity is a digest, and says so",
           one.startswith("sha256:") and len(one) == len("sha256:") + 64,
           "sha256, 64 hex digits")

    # and it survives the copy into the artifact, which is the whole point: the
    # emulator carries the identity of the dataset it was trained on.
    path = os.path.join(work, "identity.h5")
    write_artifact(path=path, text=sample_sidecar(dataset_id=one))
    blocks = read_artifact(path)
    stored = blocks[fixed_facts.FIXED_FACTS_GROUP]["dataset_id"]
    report("the emulator carries its dataset's identity, unchanged",
           stored == one,
           "recomputed from the chain and matched")


def check_mutation_arms(work):
    """Break the code on purpose, and prove the legs above go red.

    A check that cannot fail proves nothing. Each arm here reaches into the
    record's own laws, breaks one, confirms the guarding leg reds, and puts it
    back. If an arm ever stops reddening, the leg it guards has gone hollow.

    Arguments:
      work = a scratch directory to write into.

    Returns:
      None.
    """
    import h5py

    # ARM ONE: accept the old schema again. This is the mutation that undoes the
    # version bump, and the legacy-version leg is what stands against it.
    known = fixed_facts.KNOWN_VERSIONS
    fixed_facts.KNOWN_VERSIONS = known + ((2, 1, 1),)
    try:
        blocks = fixed_facts.parse_sidecar(text=sample_sidecar(),
                                           where="fixture")
        leaked = refusal_of(
            lambda: fixed_facts.validate(blocks=blocks,
                                         where="a version 2 file",
                                         schema_version=2))
    finally:
        fixed_facts.KNOWN_VERSIONS = known
    report("mutation: re-accepting the old schema version reds the version leg",
           leaked is None,
           "a version 2 file was accepted while the law was broken")

    # prove the law is back. A mutation arm that leaves the code broken has
    # turned a check into a liability.
    restored = refusal_of(
        lambda: fixed_facts.validate(
            blocks=fixed_facts.parse_sidecar(text=sample_sidecar(),
                                             where="fixture"),
            where="a version 2 file",
            schema_version=2))
    report("the version law is restored after the mutation",
           restored is not None,
           "a version 2 file refuses again")

    # ARM TWO: re-derive the blocks instead of copying them. This is the failure
    # the two-way check exists to catch: training becoming a second author of a
    # scientific fact. Here the file's blocks say one thing and the producer's
    # own text says another, exactly as a re-derivation that drifted would.
    path = os.path.join(work, "re-derived.h5")
    text = sample_sidecar()
    with h5py.File(path, "w") as f:
        f.attrs["schema_version"] = fixed_facts.SCHEMA_VERSION
        fixed_facts.write_h5(f=f, sidecar_text=text)
        # the drift: one bound, re-derived a hair wider than the producer wrote.
        f[fixed_facts.INPUT_DOMAIN_GROUP]["resolved"]["H0"][0] = "55.0"
    caught = refusal_of(lambda: read_artifact(path))
    report("mutation: a re-derived bound that drifts from the producer is caught",
           caught is not None and "does not match the producer text" in caught,
           (caught or "accepted, which is the defect").splitlines()[0][:60])

    # the control: the same file, written faithfully, still reads. An arm that
    # reds everything is not evidence either.
    clean = os.path.join(work, "faithful.h5")
    write_artifact(path=clean, text=text)
    ok = refusal_of(lambda: read_artifact(clean))
    report("the faithful copy of the same record still reads",
           ok is None,
           "the arm reds the drift, not the file")


def check_vertical_law():
    """Does the artifact belong to the cosmology being sampled?

    The artifact was generated with some coordinates held fixed: a neutrino
    mass, a curvature, a photon temperature. Those are not inputs the emulator
    takes. They are properties of the universe it learned. A chain that holds
    any of them at another value is sampling another universe, and the emulator
    will answer anyway: confidently, wrongly, and with numbers that look
    entirely ordinary.

    The law compares the coordinates the ARTIFACT pinned against the resolved
    model, and never looks at the emulator's input axes, which are the domain
    law's business. Comparing the input axes and calling it a cosmology check is
    the confusion this law exists to end.

    Returns:
      None.
    """
    blocks = blocks_of(sample_sidecar())
    held = blocks[fixed_facts.FIXED_FACTS_GROUP]["cosmology_fixed"]

    agreed = refusal_of(
        lambda: fixed_facts.check_vertical(blocks=blocks,
                                           resolved_model=SAMPLED_MODEL,
                                           where="a matching emulator"))
    report("an artifact whose pinned cosmology matches the model is accepted",
           agreed is None,
           "mnu, wa, omk, TCMB and nnu all agree")
    # the model carries w too, and the record samples w rather than pinning it.
    # A law that compared the input axes would have found w here and compared it.
    report("a coordinate the artifact sampled is not compared as a fixed fact",
           agreed is None and "w" not in held,
           "the model's w = -1.0 was never consulted")

    # the same model with the neutrino mass moved. This is a different universe,
    # and the emulator was never shown it.
    moved = dict(SAMPLED_MODEL)
    moved["mnu"] = 0.12
    message = refusal_of(
        lambda: fixed_facts.check_vertical(blocks=blocks,
                                           resolved_model=moved,
                                           where="a mismatched emulator"))
    report("an artifact born under another cosmology is refused",
           message is not None and "mnu" in message,
           (message or "accepted").splitlines()[0][:66])
    report("the refusal names the artifact's value and the sampled value",
           message is not None and "0.06" in message and "0.12" in message,
           "the record's 0.06 against the chain's 0.12")
    remedy = "Serve an emulator generated under this cosmology"
    report("the refusal says what to do about it",
           says_what_to_do(message=message, remedy=remedy),
           "names the remediation")

    # a model that never mentions the neutrino mass at all. The artifact pins it,
    # so the question "do these two agree about mnu?" has no answer, and an
    # unanswerable question is refused rather than assumed away.
    silent = dict(SAMPLED_MODEL)
    del silent["mnu"]
    message = refusal_of(
        lambda: fixed_facts.check_vertical(blocks=blocks,
                                           resolved_model=silent,
                                           where="an emulator pinning mnu"))
    report("a model silent about a coordinate the artifact pins is refused",
           message is not None and "does not say what mnu is" in message,
           (message or "accepted").splitlines()[0][:66])

    # a test double pins every coordinate at "n/a", so it disagrees with a real
    # model about all of them, and this law is where it is caught.
    double = blocks_of(double_sidecar(label="a gate's double"))
    message = refusal_of(
        lambda: fixed_facts.check_vertical(blocks=double,
                                           resolved_model=SAMPLED_MODEL,
                                           where="a test double"))
    report("a test double is refused against a real cosmology",
           message is not None and "'n/a'" in message,
           (message or "accepted, which is the defect").splitlines()[0][:66])


def check_horizontal_law():
    """Do these two artifacts belong to each other?

    Two emulators are served together all the time: a Hubble rate beside an
    angular diameter distance, a linear power spectrum beside its nonlinear
    boost. Each pair is combined into ONE prediction, so each pair must come
    from one dataset and one cosmology. Nothing checked that. The consumers
    compared parameter-axis names, which agree between two runs that share a
    prior and differ in every number that matters.

    Returns:
      None.
    """
    first_dump = "sha256:" + "a" * 64
    fresh_seed = "sha256:" + "b" * 64
    one_dump   = "sha256:" + "d" * 64

    one = blocks_of(sample_sidecar(dataset_id=first_dump))
    two = blocks_of(sample_sidecar(dataset_id=first_dump))
    same = refusal_of(
        lambda: fixed_facts.check_horizontal(blocks_a=one,
                                             blocks_b=two,
                                             where_a="the first half",
                                             where_b="the second half"))
    report("two halves of one dump may be served together",
           same is None,
           "one identity, one cosmology, one coordinate list")

    # the case comparing the facts cannot catch: the same YAML run again with a
    # fresh seed. Every fact agrees, every bound agrees, and the two are still
    # different datasets, because they drew different points.
    rerun = blocks_of(sample_sidecar(dataset_id=fresh_seed))
    message = refusal_of(
        lambda: fixed_facts.check_horizontal(blocks_a=one,
                                             blocks_b=rerun,
                                             where_a="the first half",
                                             where_b="a re-run of the same YAML"))
    report("a re-run with a new seed is refused, though every fact agrees",
           message is not None and "different datasets" in message,
           (message or "accepted, which is the defect").splitlines()[0][:66])
    report("the refusal names both dataset identities",
           message is not None and first_dump in message
           and fresh_seed in message,
           "both digests printed")

    # two records that pin the same coordinate at two values. They describe two
    # universes, and a prediction built from both halves is built from neither.
    left = blocks_of(pinned_w_sidecar(dataset_id=one_dump, w_value=-1.0))
    right = blocks_of(pinned_w_sidecar(dataset_id=one_dump, w_value=-0.9))
