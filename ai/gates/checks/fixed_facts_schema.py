def report(label, ok, detail=""):
    """Record one leg's verdict and print it.

    Arguments:
      label  = what was checked, in plain English.
      ok     = True when the check held.
      detail = the evidence, printed beside the verdict.

    Returns:
      None.
    """
    print("  [" + ("PASS" if ok else "FAIL") + "] " + label + "  (" + detail
          + ")")
    if not ok:
        FAILURES.append(label)


def emit_leg(aid, n_before):
    """Print the board's terminal line for one declared leg.

    Arguments:
      aid      = the board-declared assertion id of the leg.
      n_before = how many failures had been recorded before the leg ran.

    Returns:
      True when the leg passed.
    """
    mark = "PASS" if len(FAILURES) == n_before else "FAIL"
    print("##AID " + aid + " " + mark)
    return mark == "PASS"


def emit_unavailable(aid, blocker):
    """Print the reserved terminal for a declared leg that never ran.

    A leg skipped because an earlier leg failed did not fail: it did not run at
    all, and a verdict it never produced must not read as one it did.

    Arguments:
      aid     = the board-declared assertion id of the leg that never ran.
      blocker = the aid of the upstream leg whose failure stopped it, or None
                when the check exited before any leg reached its terminal.

    Returns:
      None.
    """
    if blocker is None:
        reason = "the child exited before this leg ran"
    else:
        reason = "upstream leg " + blocker + " did not pass"
    print("##AID " + aid + " UNAVAILABLE " + reason)


def facts_sidecar(dataset_id, names, requested, resolved):
    """Build a record under the fixture's one cosmology, over the given names.

    The equation-of-state parameter w is SAMPLED by this fixture, so it must not
    also be pinned among the fixed facts; wa is fixed at zero and unsampled, so
    it may be. That asymmetry is the point of the fixture.

    Every record built through here carries the SAME fixed facts, written in one
    place. That matters for the horizontal law below, whose fixtures must differ
    in exactly one thing at a time: if two of them drifted apart on some fact
    nobody was looking at, the law would refuse for a reason the leg never meant
    to test, and the leg would still be green.

    Arguments:
      dataset_id = the identity to record, so a caller can build two records
                   that agree or disagree about which dataset they came from.
      names      = the sampled parameters, in the generator's canonical order.
      requested  = mapping name -> (low, high), the support the prior asked for.
      resolved   = mapping name -> (low, high), the support drawn from.

    Returns:
      the sidecar's text.
    """
    return fixed_facts.build_sidecar(
        dataset_id=dataset_id,
        generator="dataset_generator_mps",
        family="grid2d",
        cosmology_fixed={"mnu":  0.06,
                         "wa":   0.0,
                         "omk":  0.0,
                         "TCMB": 2.7255,
                         "nnu":  3.044},
        neutrino_convention="degenerate",
        flat_only=True,
        dark_energy_law="constant-w",
        dark_energy_inputs=["w"],
        cl_units=fixed_facts.NOT_APPLICABLE,
        base_identity="syren analytic base, mnu pinned at 0.06",
        names=names,
        requested=requested,
        resolved=resolved)


def sample_sidecar(dataset_id="sha256:" + "a" * 64):
    """Build the record a real matter-power dataset would publish.

    Arguments:
      dataset_id = the identity to record, so a caller can build two records
                   that agree or disagree about which dataset they came from.

    Returns:
      the sidecar's text.
    """
    return facts_sidecar(dataset_id=dataset_id,
                         names=NAMES,
                         requested=REQUESTED,
                         resolved=RESOLVED)


def half_without_w_sidecar(dataset_id):
    """Build the record a half that never sampled the equation of state writes.

    Every fixed fact is identical to the record above, on purpose. The two
    differ in one thing only, the list of sampled coordinates, so the only law
    that can refuse the pair is the one that compares those lists.

    Arguments:
      dataset_id = the identity to record.

    Returns:
      the sidecar's text.
    """
    support = {}
    for name in NAMES_WITHOUT_W:
        support[name] = RESOLVED[name]
    return facts_sidecar(dataset_id=dataset_id,
                         names=NAMES_WITHOUT_W,
                         requested=support,
                         resolved=support)


def box_sidecar(dataset_id, omegam_support):
    """Build a record whose omegam was drawn from the caller's interval.

    The served-support law reads the RESOLVED support only, so the requested one
    is written to match it here. The endpoint stretch that separates the two in
    a real dataset plays no part in this law.

    Arguments:
      dataset_id     = the identity to record.
      omegam_support = (low, high), the interval omegam was drawn from.

    Returns:
      the sidecar's text.
    """
    support = {}
    for name in NAMES:
        support[name] = RESOLVED[name]
    support["omegam"] = omegam_support
    return facts_sidecar(dataset_id=dataset_id,
                         names=NAMES,
                         requested=support,
                         resolved=support)


